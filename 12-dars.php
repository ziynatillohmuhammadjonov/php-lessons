    case 1:
        echo 'Bir baho';
        break;
    case 2:
        echo 'Ikki baho';
        break;
    case 3:
        echo 'Uch baho';
        break;
    case 4:
        echo 'To\'rt baho';
        break;
    case 5:
        echo 'Besh baho';
        break;
    default:
        echo 'Siz baho olmadingiz.';
        break;
}
